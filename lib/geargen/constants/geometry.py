"""Geometric constants for gear generation.

All angle values are in radians (Python's math library convention).
"""

import math

# Common Angles (in radians)
ANGLE_90_DEG = math.pi / 2  # Right angle (π/2 radians)
ANGLE_180_DEG = math.pi  # Straight angle (π radians)
ANGLE_360_DEG = math.pi * 2  # Full circle (2π radians)

# Standard Reference Angles for Text Placement (in radians)
ANGLE_15_DEG = math.radians(15)  # 15 degrees
ANGLE_30_DEG = math.radians(30)  # 30 degrees
ANGLE_45_DEG = math.radians(45)  # 45 degrees
ANGLE_60_DEG = math.radians(60)  # 60 degrees
